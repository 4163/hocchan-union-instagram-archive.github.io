// Function to update currentAffixIndex and currentFilename based on the currentIndex media
function updateCurrentFilenameAndAffix() {
	const source = mediaSources[currentIndex];
	const match = source.match(/_(\d+)\./);
	
	if (match) {
		currentAffixIndex = currentIndex;
		currentFilename = source.replace(/_\d+\.\w+$/, ''); // Remove affix and file extension
	} else {
		currentAffixIndex = -1;
		currentFilename = source.replace(/\.\w+$/, ''); // Remove file extension only
	}
}

// Function to check if the current media source has an affix of _1 or higher 
function checkAffixVisibility() {
	const source = mediaSources[currentIndex];
	const match = source.match(/_(\d+)\./);

	// Select the sidebar button container
	const sidebarButtonContainer = document.querySelector('.button-sidebar');

	// Show sidebar buttons if affix is 1 or greater
	if (match && parseInt(match[1]) >= 1) {
		sidebarButtonContainer.style.display = 'block';
	} else {
		sidebarButtonContainer.style.display = 'none';
	}
}

// Function to check if the current media source has an affix greater than 1
function hasAffixGreaterThan1(index) {
	const source = mediaSources[index];
	const match = source.match(/_(\d+)\./);
	return match && parseInt(match[1]) > 1;
}

// Function to find the nearest main image without an affix greater than 1
function findNearestMainImage() {
	while (hasAffixGreaterThan1(currentIndex)) {
		currentIndex = (currentIndex < mediaSources.length - 1) ? currentIndex + 1 : 0;
	}
}

// Function to update the media (image or video) displayed in the slider
function updateMedia() {
	const container = document.querySelector('.image-container');
	container.innerHTML = ''; // Clear the current content

	const source = mediaSources[currentIndex];
	const mediaElement = source.endsWith('.mp4') ? document.createElement('video') : document.createElement('img');

	if (source.endsWith('.mp4')) {
		mediaElement.src = source;
		mediaElement.controls = true;
		mediaElement.autoplay = true;
		mediaElement.volume = 0.15; // Set volume to 15%
	} else {
		mediaElement.src = source;
	}

	mediaElement.style.opacity = 1; // Ensure the opacity is set to 1
	container.appendChild(mediaElement);

	// Update likes, comments, timestamp, and description
	document.querySelector('.timestamp').textContent = timestampsArray[currentIndex];

	// Update current affix index after changing the media
	updateCurrentFilenameAndAffix();

	// Check if the sidebar buttons should be visible
	checkAffixVisibility();
}

